var colorOut = "";
var counter = $(".pointBall").length;

if (counter > 8){colorOut = '#224f94';}
if (counter == 8){colorOut = '#224f94';}
if (counter == 6){colorOut = '#224f94';}
if (counter == 5){colorOut = '#224f94';}

$("#serv1").click(function() {
  window.location.href = 'servicos/serv1.html';
});

$("#serv1").hover(function() {
  $(this).children().children('span').children().css('background-color', 'black');
});

$("#serv1").mouseleave(function() {
  $(this).children().children('span').children().css('background-color', colorOut);   
});

